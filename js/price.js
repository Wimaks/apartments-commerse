const rowElement = (title, price) => `
        <li class="price__list-link">
            <span class="service__name">${title}</span>
            <span class="service__price">${price}</span>
        </li>
  `

const btnsContainer = document.querySelector('.service__list');
const infoContainer = document.querySelector('.price__list.content')
let activeCategory = 1;

btnsContainer.addEventListener('click', (e) => {
    // const parent = e.target.parentElement
    const element = e.target.closest('.service__list-link')
    // console.log(e.currentTarget.childNodes);
    if (element.tagName === 'LI') {
        btnsContainer.querySelectorAll('li').forEach(li => li.classList.remove('active'))
        element.classList.add('active')
        activeCategory = +element.dataset.index
        showElements()
    }
})

function showElements() {
    infoContainer.innerHTML = '';

    data[activeCategory - 1].map(item => infoContainer.innerHTML += rowElement(item.title, item.price))
}

showElements()